import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const root = resolve(scriptDir, '..')
const contractPath = resolve(root, 'backend/internal/authz/policy.contract.json')
const outputPath = resolve(
  root,
  'packages/app/features/auth/entity-authz.generated.ts'
)
const checkOnly = process.argv.includes('--check')

const ensureStringArray = (value, label) => {
  if (!Array.isArray(value) || value.length === 0) {
    throw new Error(`"${label}" phải là mảng không rỗng`)
  }
  value.forEach((item, index) => {
    if (typeof item !== 'string' || item.trim() === '') {
      throw new Error(`"${label}"[${index}] phải là string không rỗng`)
    }
  })
}

const normalizeContract = (input) => {
  if (!input || typeof input !== 'object') {
    throw new Error('Authz contract phải là object')
  }

  const version = Number(input.version ?? 1)
  if (!Number.isFinite(version) || version <= 0) {
    throw new Error('"version" phải là số nguyên dương')
  }

  const roles = [...new Set(input.roles ?? [])]
  const actions = [...new Set(input.actions ?? [])]
  ensureStringArray(roles, 'roles')
  ensureStringArray(actions, 'actions')

  const actionSet = new Set(actions)
  const policyInput = input.policies
  if (!policyInput || typeof policyInput !== 'object') {
    throw new Error('"policies" phải là object')
  }

  const policies = {}
  roles.forEach((role) => {
    const rolePolicy = policyInput[role]
    if (!rolePolicy || typeof rolePolicy !== 'object') {
      throw new Error(`Thiếu policy cho role "${role}"`)
    }
    const entityEntries = Object.entries(rolePolicy)
    if (entityEntries.length === 0) {
      throw new Error(`Role "${role}" phải có ít nhất 1 entity policy`)
    }

    const normalizedRolePolicy = {}
    entityEntries
      .sort(([a], [b]) => a.localeCompare(b))
      .forEach(([entity, allowedActions]) => {
        if (typeof entity !== 'string' || entity.trim() === '') {
          throw new Error(`Role "${role}" có entity key không hợp lệ`)
        }
        ensureStringArray(allowedActions, `policies.${role}.${entity}`)
        const dedupActions = [...new Set(allowedActions)]
        dedupActions.forEach((action) => {
          if (!actionSet.has(action)) {
            throw new Error(
              `Role "${role}" entity "${entity}" có action không khai báo: "${action}"`
            )
          }
        })
        normalizedRolePolicy[entity] = dedupActions
      })
    policies[role] = normalizedRolePolicy
  })

  return {
    version,
    roles,
    actions,
    policies,
  }
}

const buildOutput = (contract) => {
  const roleLiteral = JSON.stringify(contract.roles, null, 2)
  const actionLiteral = JSON.stringify(contract.actions, null, 2)
  const policyLiteral = JSON.stringify(contract.policies, null, 2)

  return `/* eslint-disable */
// Code generated by scripts/generate-authz-contract.mjs. DO NOT EDIT.

export const ENTITY_AUTHZ_VERSION = ${contract.version} as const

export const ENTITY_AUTHZ_ROLES = ${roleLiteral} as const
export type EntityAuthzRole = (typeof ENTITY_AUTHZ_ROLES)[number]

export const ENTITY_AUTHZ_ACTIONS = ${actionLiteral} as const
export type EntityAuthzAction = (typeof ENTITY_AUTHZ_ACTIONS)[number]

export const ENTITY_AUTHZ_POLICY = ${policyLiteral} as const satisfies Record<
  EntityAuthzRole,
  Record<string, readonly EntityAuthzAction[]>
>

const ENTITY_ACTION_SET = new Set<EntityAuthzAction>(ENTITY_AUTHZ_ACTIONS)

export const isEntityAuthzAction = (
  action: string
): action is EntityAuthzAction => ENTITY_ACTION_SET.has(action as EntityAuthzAction)
`
}

const rawContract = readFileSync(contractPath, 'utf8')
const parsedContract = JSON.parse(rawContract)
const contract = normalizeContract(parsedContract)
const expectedContent = buildOutput(contract)

if (checkOnly) {
  if (!existsSync(outputPath)) {
    throw new Error(`Missing generated file: ${outputPath}`)
  }
  const currentContent = readFileSync(outputPath, 'utf8')
  if (currentContent !== expectedContent) {
    throw new Error(
      'entity-authz.generated.ts is out of date. Run: npm run authz:sync'
    )
  }
  process.stdout.write('Authz contract check passed\n')
} else {
  writeFileSync(outputPath, expectedContent, 'utf8')
  process.stdout.write('Generated entity authz contract for frontend\n')
}
